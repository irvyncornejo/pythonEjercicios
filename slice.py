""" #Slice en python
Python tiene una de las sintaxis más poderosas para manipular secuncias
Esta sintaxis se llama slice(rebanadas en español)
    secuncia [comienzo: final : pasos]
Ej. """
my_favorite_fruit = 'watermelon'
my_favorite_fruit[0] #'w'
my_favorite_fruit[-1] # 'n'
my_favorite_fruit[0:3] #  'wat'
my_favorite_fruit[::2] # 'wtreo'
my_favorite_fruit[3:] #'ermelon'
my_favorite_fruit[:]#'watermelon'
my_favorite_fruit[1:-1:2]# 'aeml'
my_favorite_fruit[::-1] #'nolemretaw'

